# Utilizando loops For, saluda a todos los miembros de una clase, imprimiendo "Hola" + su nombre.
# Por ejemplo: "Hola María"
# alumnos_clase = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]

alumnos_clase = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]

for alumno in alumnos_clase:
    print("Hola " + alumno)